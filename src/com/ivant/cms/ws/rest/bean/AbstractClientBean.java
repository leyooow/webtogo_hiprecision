package com.ivant.cms.ws.rest.bean;

import javax.xml.bind.annotation.XmlElement;

/**
 * @author Edgar S. Dacpano
 */
public class AbstractClientBean
{
	private String message;
	private Long companyId;
	private String broadcastDate;
	private String endDate;
	
	public AbstractClientBean()
	{
		super();
	}
	
	/**
	 * @return the message
	 */
	@XmlElement(nillable = false)
	public String getMessage()
	{
		return message;
	}
	
	/**
	 * @return the companyId
	 */
	@XmlElement(nillable = false)
	public Long getCompanyId()
	{
		return companyId;
	}
	
	/**
	 * @return the broadcastDate
	 */
	@XmlElement(nillable = false)
	public String getBroadcastDate()
	{
		return broadcastDate;
	}
	
	/**
	 * @return the endDate
	 */
	@XmlElement(nillable = false)
	public String getEndDate()
	{
		return endDate;
	}
	
	// SETTERS
	
	/**
	 * @param message the message to set
	 */
	public void setMessage(String message)
	{
		this.message = message;
	}
	
	/**
	 * @param companyId the companyId to set
	 */
	public void setCompanyId(Long companyId)
	{
		this.companyId = companyId;
	}
	
	/**
	 * @param broadcastDate the broadcastDate to set
	 */
	public void setBroadcastDate(String broadcastDate)
	{
		this.broadcastDate = broadcastDate;
	}
	
	/**
	 * @param endDate the endDate to set
	 */
	public void setEndDate(String endDate)
	{
		this.endDate = endDate;
	}
	
}
