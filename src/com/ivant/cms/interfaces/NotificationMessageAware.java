package com.ivant.cms.interfaces;

/**
 * Used in conjunction with NotificationMessageInterceptor. 
 * The Notification Message Interceptor calls the action (of these type) 
 * and set the Notification Message.
 * 
 * @author Mark Kenneth M. Rañosa
 *
 */
public interface NotificationMessageAware {
	
	/**
	 * Sets the notification message with the specified parameter.
	 * 
	 * @param notificationMessage
	 * 			- message to notify the user
	 */
	public void setNotificationMessage(String notificationMessage);
}
