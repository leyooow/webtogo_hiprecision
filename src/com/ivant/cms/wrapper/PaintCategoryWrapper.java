package com.ivant.cms.wrapper;

import com.ivant.cms.entity.baseobjects.BaseObject;

/**
 * 
 * @author Kent
 *
 */
public class PaintCategoryWrapper extends BaseObject
{
	private String code;
	private String name;
	private String imageUrl;
	private boolean isColorizer;
	private Long parentCategory;
	private boolean hasColorizerImageCategory;
	private boolean isCustomPaint;
	
	public static final String [] FIELDS_PAINT_CATEGORY = {
		"id",
		"created_on",
		"valid",
		"updated_on",
		"code",
		"name",
		"image_url",
		"is_colorizer",
		"parent_category",
		"has_colorizer_image_category",
		"is_custom_paint"
	};
	
	public PaintCategoryWrapper()
	{
		
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public boolean getIsColorizer() {
		return isColorizer;
	}

	public void setIsColorizer(boolean isColorizer) {
		this.isColorizer = isColorizer;
	}

	public Long getParentCategory() {
		return parentCategory;
	}

	public void setParentCategory(Long parentCategory) {
		this.parentCategory = parentCategory;
	}

	public boolean getHasColorizerImageCategory() {
		return hasColorizerImageCategory;
	}

	public void setHasColorizerImageCategory(boolean hasColorizerImageCategory) {
		this.hasColorizerImageCategory = hasColorizerImageCategory;
	}

	public boolean getIsCustomPaint() {
		return isCustomPaint;
	}

	public void setIsCustomPaint(boolean isCustomPaint) {
		this.isCustomPaint = isCustomPaint;
	}
}
