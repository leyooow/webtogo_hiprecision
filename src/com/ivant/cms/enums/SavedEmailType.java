package com.ivant.cms.enums;

public enum SavedEmailType {
	SAVED("Saved"),
	NOTIFICATION("Notification"),
	REGISTRATION("Registration"),
	CONFIRMATION("Confirmation"),
	WARNING("Warning"),
	FAILED("Failed");
	
	private String value;
	
	SavedEmailType(String value) {
		this.value = value;
	}
	
	public String getValue() {
		return value;
	}
}
