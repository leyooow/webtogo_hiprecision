public enum AppearanceType
{
	TEST_PROCEDURE ("Test Procedure"),
	PROTOCOL ("Protocol");
	
	private String value;
	
	AppearanceType(String value) 
	{
		this.value = value;
	}
	
	public String getValue() {
		return value;
	}
	
	@Override
	public String toString() {
		return value;
	}
}